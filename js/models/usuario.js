class Usuario {
    nombre;
    apellido;
    email;
    telefono;
    contrasenna;

    constructor(nombre, apellido, email, telefono, contrasena) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.email = email;
        this.telefono = telefono;
        this.contrasenna = contrasena;
    }
}